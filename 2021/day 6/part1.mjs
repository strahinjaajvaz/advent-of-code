function solve(nums, n) {
  let zerosFound = 0;
  let arr = [...nums];

  for (let i = 0; i < n; i++) {
    for (let j = 0; j < arr.length; j++) {
      if (arr[j] === 0) {
        zerosFound++;
        arr[j] = 6;
      } else arr[j]--;
    }

    arr = arr.concat(Array(zerosFound).fill(8));
    zerosFound = 0;
  }

  return arr.length;
}

